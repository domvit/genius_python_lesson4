# 2. Об'єднання кортежів
print("Завдання 2: Об'єднання кортежів")
tuple1 = (1, 2, 3)  # Перший кортеж
tuple2 = (4, 5, 6)  # Другий кортеж
print(f"Перший кортеж: {tuple1}")
print(f"Другий кортеж: {tuple2}")

combined_tuple = tuple1 + tuple2  # Об'єднуємо кортежі
print(f"Об'єднаний кортеж: {combined_tuple}")